package Classes;

public class primitiveNodeData {
    private String pos;
    private int id;

    public primitiveNodeData(String pos, int ID) {
        this.pos = pos;
        this.id = ID;
    }

    public String getPos() {
        return pos;
    }

    public void setPos(String pos) {
        this.pos = pos;
    }

    public int getID() {
        return id;
    }

    public void setID(int ID) {
        this.id = ID;
    }
}
